//Operações básicas em JS
let ct = 0;
let a = ct++; //incremento no ct e então atribuição à variável a, portanto ct = 1
console.log("valor de a: ", a)
console.log("valor de ct: ", ct)

let b = ++ct; //aqui ct já virou 1, e então ele receberá um incremete antes, é como se fosse escrito 1+ct, neste caso 2, e então o valor é atribuído à b.
console.log("Valor de b: ", b)
console.log("Valor de ct: ", ct)

